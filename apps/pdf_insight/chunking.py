def chunk_text(text, chunk_size=800, overlap=100):
    """Splits raw report text into overlapping chunks for embedding."""
    text = text.strip()
    if not text:
        return []

    chunks = []
    start = 0
    while start < len(text):
        end = start + chunk_size
        chunk = text[start:end].strip()
        if chunk:
            chunks.append(chunk)
        start += chunk_size - overlap
    return chunks